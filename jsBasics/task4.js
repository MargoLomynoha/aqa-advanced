// subtask1
const piNumber = Math.PI;
const circleRadius = 32;

const circleArea = piNumber * Math.pow(circleRadius, 2);
console.log(`Circle area is ${circleArea.toFixed(2)}.`);

// subtask2

const rectLength = 26.6;
const rectWidth = 42;

const rectangleArea = rectLength * rectWidth;
console.log(`Rectangle area is ${rectangleArea.toFixed(2)}.`);

// subtask3

const cylinderRadius = 12;
const cylinderHeight = 8;

const cylinderVolume = piNumber * Math.pow(cylinderRadius, 2) * cylinderHeight;
console.log(`Cylinder volume is ${cylinderVolume.toFixed(2)}.`);
